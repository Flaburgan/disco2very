// We have to make some computation on the data to make it better for users
// Functions below are only doing that

type ComputeItemFunc = (
  element: AdemeECV,
  coeff: number,
  locale: Locale,
) => { label: string; description: string; explanation: string };

function computeItemsForCategory(
  categoryId: number,
  data: RawEquivalent[],
  locale: Locale,
): Item[] {
  const computeItemFunc: ComputeItemFunc = getComputeFunction(categoryId);
  const items: Item[] = [];
  for (const raw of data) {
    const element = toAdemeECV(raw, locale);
    if (element === undefined) {
      continue;
    }
    const coeff = getCoefficient(categoryId, element);
    const source = withCoefficient(element, coeff);
    items.push({
      id: source.slug,
      categoryId: categoryId,
      source,
      ...computeItemFunc(source, coeff, locale),
    });
  }
  return items;
}

// Turns a raw impactco2 equivalent into the shape used by the game, resolving
// the translated name and hypothesis and computing the total footprint the
// same way impactco2 does (src/utils/computeECV.ts).
function toAdemeECV(raw: RawEquivalent, locale: Locale): AdemeECV | undefined {
  const name = getItemName(raw.slug);
  if (name === undefined) {
    console.warn(`No "${locale}" name for "${raw.slug}", skipping it`);
    return undefined;
  }

  const footprint = raw.ecv
    ? raw.ecv.reduce((sum, { value }) => sum + value, 0)
    : (raw.total ?? 0);
  const usage = raw.usage ? raw.usage.peryear * raw.usage.defaultyears : 0;

  const { hypothesis } = data();
  const hypothesisText = [hypothesis.pre[raw.slug], hypothesis.post[raw.slug]]
    .filter(Boolean)
    .join(" ");

  return {
    name,
    slug: raw.slug,
    ecv: footprint + usage + (raw.end ?? 0),
    footprint,
    footprintDetail: raw.ecv,
    usage: raw.usage,
    endOfLife: raw.end,
    months: raw.months,
    hypothesis: hypothesisText === "" ? undefined : hypothesisText,
    sources: raw.sources,
  };
}

// The names extracted from impactco2 are "quantified" ("kg de ;fraise",
// "smartphone[s]"): the part before ";" is a unit prefix and "[s]"/"[es]"/"[x]"
// are plural markers, both of which we drop.
function getItemName(slug: string): string | undefined {
  const name = data().names[slug];
  if (name === undefined) {
    return undefined;
  }
  const cleaned = name
    .split(";")
    .pop()!
    .replace(/\[(s|es|x)\]/g, "")
    .trim();
  return cleaned.charAt(0).toUpperCase() + cleaned.slice(1);
}

function getCoefficient(categoryId: number, element: AdemeECV): number {
  switch (categoryId) {
    case 4: // Transport: from 1km to a typical journey length
      return (transportDistances as { [slug: string]: number })[element.slug];
    case 8: // Heating: from 1m² to 60m², from 12 months to 1 month
      return 60 / 12;
    default:
      return 1;
  }
}

// Returns a scaled copy: the imported data objects are shared module state
// and must never be mutated.
function withCoefficient(element: AdemeECV, coeff: number): AdemeECV {
  const copy: AdemeECV = {
    ...element,
    ecv: element.ecv * coeff,
    footprintDetail: element.footprintDetail?.map((detail) => ({
      ...detail,
      value: detail.value * coeff,
    })),
    usage: element.usage ? { ...element.usage } : undefined,
  };
  if (copy.footprint !== undefined) {
    copy.footprint = copy.footprint * coeff;
  }
  return copy;
}

function getComputeFunction(categoryId: number): ComputeItemFunc {
  switch (categoryId) {
    case 1: // Digital
      return (element: AdemeECV) => {
        return {
          label: element.name,
          description: getDescriptionForDefaultYear(element),
          explanation: "",
        };
      };
    case 2: // Food
      return (element: AdemeECV) => {
        return {
          label:
            element.name + (element.slug.startsWith("repas") ? "" : " (1kg)"),
          description: "",
          explanation: "",
        };
      };
    case 3: // Drinks
      return (element: AdemeECV) => {
        return {
          label: element.name + " (1L)",
          description: "",
          explanation: "",
        };
      };
    case 4: // Transport
      return (element: AdemeECV, coeff: number) => {
        return {
          label: element.name + ` (${coeff}km)`,
          description: "",
          explanation: "",
        };
      };
    case 6: // Household appliance
    case 7: // Furnitures
      return (element: AdemeECV) => {
        return {
          label: element.name,
          description: getDescriptionForDefaultYear(element),
          explanation: "",
        };
      };
    case 8: // Heating
      return (element: AdemeECV) => {
        return {
          label: element.name,
          description: t`<strong>per month</strong> for 60m²`,
          explanation: t`Impact of one month of heating a house of 60m², spreading the consumption over the year.`,
        };
      };
    case 9: // Vegetables and fruits
      return (element: AdemeECV, _coeff: number, locale: Locale) => {
        return {
          label: element.name + " (1kg)",
          description: getSeasonDescription(element.months, locale),
          explanation: "",
        };
      };
    // case 10: // TODO Usage numérique needs more work
    default: // Clothing, practical cases
      return (element: AdemeECV) => {
        return {
          label: element.name,
          description: "",
          explanation: "",
        };
      };
  }
}

function getDescriptionForDefaultYear(element: AdemeECV) {
  let description = "";
  if (element.usage) {
    const defaultYears = element.usage.defaultyears;
    description = t`Purchase and usage for ${defaultYears} years.`;
  }
  return description;
}

function getSeasonDescription(
  months: number[] | undefined,
  locale: Locale,
): string {
  if (!months || months.length === 0) {
    return "";
  }
  if (months.length === 12) {
    return t`In season all year round`;
  }

  // Group consecutive months into ranges, merging December -> January.
  const sorted = [...new Set(months)].sort((a, b) => a - b);
  const ranges: number[][] = [[sorted[0]]];
  for (const month of sorted.slice(1)) {
    const range = ranges[ranges.length - 1];
    if (month === range[range.length - 1] + 1) {
      range.push(month);
    } else {
      ranges.push([month]);
    }
  }
  const first = ranges[0];
  const last = ranges[ranges.length - 1];
  if (ranges.length > 1 && first[0] === 0 && last[last.length - 1] === 11) {
    ranges[0] = [...last, ...first];
    ranges.pop();
  }

  const monthFormat = new Intl.DateTimeFormat(locale, { month: "long" });
  const monthName = (month: number) =>
    monthFormat.format(new Date(2000, month, 1));
  const season = ranges
    .map((range) =>
      range.length === 1
        ? monthName(range[0])
        : `${monthName(range[0])} – ${monthName(range[range.length - 1])}`,
    )
    .join(", ");
  return t`In season: ${season}`;
}
