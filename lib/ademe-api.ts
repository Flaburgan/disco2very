interface AdemeLocale {
  names: { [slug: string]: string };
  hypothesis: {
    pre: { [slug: string]: string };
    post: { [slug: string]: string };
  };
  ecv: { [id: string]: string };
}

// fr/en/es are generated by scripts/update-ademe-data.mjs; German is not
// translated upstream and de.json is maintained by hand in this repository.
// Each file is its own chunk: only the active locale's is ever downloaded.
const localeImports: {
  [L in Locale]: () => Promise<{ default: AdemeLocale }>;
} = {
  en: () => import("../data/ademe/locales/en.json"),
  fr: () => import("../data/ademe/locales/fr.json"),
  es: () => import("../data/ademe/locales/es.json"),
  de: () => import("../data/ademe/locales/de.json"),
};

let localeData: AdemeLocale | null = null;

// Must have completed before any other function of this module is called:
// components/app.tsx starts it at boot and suspends until it resolves.
export async function loadLocaleData(locale: Locale): Promise<void> {
  localeData = (await localeImports[locale]()).default;
}

function data(): AdemeLocale {
  if (localeData === null) {
    throw new Error("loadLocaleData() has not completed yet");
  }
  return localeData;
}

// Transports are declined in many variants upstream (city car, sedan...):
// only the ones with a defined journey length enter the game.
function flattenTransports(equivalents: RawEquivalent[]): RawEquivalent[] {
  const flattened = equivalents.flatMap((equivalent) =>
    equivalent.ecvs
      ? equivalent.ecvs.map((sub) => ({
          ...equivalent,
          ecvs: undefined,
          ecv: sub.ecv,
          slug: `${equivalent.slug}-${sub.subtitle}`
            .replace(/ /g, "")
            .toLowerCase(),
        }))
      : [equivalent],
  );
  const bySlug = new Map(
    flattened.map((equivalent) => [equivalent.slug, equivalent]),
  );
  const transports: RawEquivalent[] = [];
  for (const slug of Object.keys(transportDistances)) {
    const transport = bySlug.get(slug);
    if (transport) {
      transports.push(transport);
    } else {
      console.warn(`Transport "${slug}" is missing from the ADEME data`);
    }
  }
  return transports;
}

